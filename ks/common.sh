#!/bin/sh

# ディレクトリ作成
/bin/mkdir -p ~/{src,backup}

####################################################################################################
# 古いバージョンの削除する関数
####################################################################################################
uninstall()
{
    if [ $1 ]; then
        if [ -f /usr/local/bin/paco ]; then
            /usr/local/bin/paco -r `paco -1a | grep $1`;
        fi
    fi
}

####################################################################################################
# ファイルをバックアップする関数
####################################################################################################
backup_file()
{
  if [ -f $1 ]; then
    /bin/cp $1 ~/backup/`basename $1`.`date '+%Y%m%d-%H%M%S'`
  fi
}
